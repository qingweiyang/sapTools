package com.sap.datastruct;

public class Properties {
	private int station_no;
	
	private int heading;
	
	private String name;
	
	private int stat;

	private int lane_no;
	
	private String t_stamp;
	
	private long segment_id;
	
	private String district_id_name;
	
	private int taz_id;
	
	private String color;

	public int getStation_no() {
		return station_no;
	}

	public void setStation_no(int station_no) {
		this.station_no = station_no;
	}

	public int getHeading() {
		return heading;
	}

	public void setHeading(int heading) {
		this.heading = heading;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public int getStat() {
		return stat;
	}

	public void setStat(int stat) {
		this.stat = stat;
	}

	public int getLane_no() {
		return lane_no;
	}

	public void setLane_no(int lane_no) {
		this.lane_no = lane_no;
	}

	public String getT_stamp() {
		return t_stamp;
	}

	public void setT_stamp(String t_stamp) {
		this.t_stamp = t_stamp;
	}

	public long getSegment_id() {
		return segment_id;
	}

	public void setSegment_id(long segment_id) {
		this.segment_id = segment_id;
	}

	public String getDistrict_id_name() {
		return district_id_name;
	}

	public void setDistrict_id_name(String district_id_name) {
		this.district_id_name = district_id_name;
	}

	public int getTaz_id() {
		return taz_id;
	}

	public void setTaz_id(int taz_id) {
		this.taz_id = taz_id;
	}

	public String getColor() {
		return color;
	}

	public void setColor(String color) {
		this.color = color;
	}

}
